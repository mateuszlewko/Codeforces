#define _upgrade do { ios::sync_with_stdio(0); cin.tie(0); } while (0)

typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;

const int N = 200 * 1000 + 10;
int A[N];
int even_to_right[N];
int pref_sum[N];

int main() {
	_upgrade;

	int n;
	cin >> n;

	For (i, n) {
		int x;
		cin >> x;
		A[i] = x < 0 ? 1 : 0;
	}

	For (i, n) {
		pref_sum[i + 1] = pref_sum[i] + A[i]; 
	}

	for (int i = n; i >= 1; i--) {
		even_to_right[i] = even_to_right[i + 1] + int(pref_sum[i] % 2 == 0);
	}

	ll even_cnt = 0;
	bool take_even = true;
	for (int i = 1; i <= n; i++) {
		if (A[i - 1] == 1) take_even = !take_even;
		if (take_even) {
			even_cnt += even_to_right[i];
		} else {
			even_cnt += n - i - even_to_right[i];
		}
	}

	ll all = (ll)n * (ll)(n + 1) / 2LL;
	cout << all - even_cnt << " " << even_cnt << "\n"; 
}

