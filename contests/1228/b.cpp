#define _upgrade do { ios::sync_with_stdio(0); cin.tie(0); } while (0)

typedef long long ll;
typedef long double ld;
typedef pair<int, int> pii;
typedef pair<ll, ll> pll;

const int N = 1010;
const ll M = 1000 * 1000 * 1000 + 7;
int A[N][N];

int main() {
	_upgrade;

	int n, m;
	cin >> n >> m;

	bool wrong = false;

	For (i, n) {
		int r;
		cin >> r;

		For (j, r) {
			if (A[i][j] == 1) wrong = true;
			A[i][j] = 2;
		}

		if (A[i][r] == 2) wrong = true;
		A[i][r] = 1;
	}

	For (i, m) {
		int c;
		cin >> c;

		For (j, c) {
			if (A[j][i] == 1) wrong = true;
			A[j][i] = 2;
		}

		if (A[c][i] == 2) wrong = true;
		A[c][i] = 1;
	}

	if (wrong) {
		cout << "0\n";
		return 0;
	}

	ll res = 1;
	For (i, n) {
		For (j, m) {
			if (A[i][j] == 0) {
				res = (res * 2LL) %  M;
			}
		}
	}

	cout << res << "\n";
}

