int main() {
    _upgrade;

	string t;
	cin >> t;

	int n = t.size();

	int a_cnt = 0;
	for (char c : t) {
		if (c == 'a') a_cnt++;
	}

	int s2 = (n + a_cnt);
	if (s2 % 2 != 0) {
		wrong();
	}

	int s_size = s2 / 2;
	string s_without_a = "";

	string s = "";

	string s_prim = "";

	For (i, n) {
		if (i < s_size) {
			s.push_back(t[i]);
			if (t[i] != 'a') s_without_a.push_back(t[i]);
		} else {
			if (t[i] == 'a') {
				wrong();
			}

			s_prim.push_back(t[i]);
		}
	}

	if (s_without_a != s_prim) {
		wrong();
	} else {
		cout << s << "\n";
	}
}

